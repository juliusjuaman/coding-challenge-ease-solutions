
public class PathDetails {

	int startHeight, endHeight, length, xNext, yNext;
	
	public PathDetails(int startHeight, int endHeight, int length, int xNext, int yNext) {
		this.startHeight = startHeight;
		this.endHeight = endHeight;
		this.length = length;
		this.xNext = xNext;
		this.yNext = yNext;
	}
}
